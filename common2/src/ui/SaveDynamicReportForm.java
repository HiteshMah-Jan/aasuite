private void lstReportMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstReportMouseClicked
    if (SwingUtilities.isRightMouseButton(evt)) {
        return;
    }
    int index = lstReport.getSelectedIndex();
    DynamicReport obj = dynamicReportList.get(index);
    if (obj==null || obj.getFile()==null) {
        PanelUtil.showError(jPanel1, "Please select a valid report file");
        return;
    }
    if (evt.getClickCount()==2) {
        ReportDesigner.showMe(obj.getFile());
    }
    JasperReport report = AbstractReport.compileReport(obj.getFile());
    List<JRParameter> params = AbstractReport.getReportParameters(report);
    if (params!=null && params.size()>0) {
        reportParameterViewer1.showParameters(report, pnlReportViewer, obj.getFile());
    }
    else {  
        AbstractReport.displayReportToPanel(pnlReportViewer, report);
    }
}//GEN-LAST:event_lstReportMouseClicked
    
    
