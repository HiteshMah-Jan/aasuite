    private void reloadAllFiles() {
        PanelUtil.clearList(dynamicReportList);
        File[] files = component.reportdesigner.ReportDesigner.getDesignFiles();
        for (File f:files) {
            try {
                if (f.getName().endsWith("bak")) continue;
                bean.admin.DynamicReport e = new bean.admin.DynamicReport();
                e.setCode(f.getName());
                e.setReportName(f.getPath());
                e.setXmlContent(new String(util.DataUtil.getBytes(f)));
                e.setFile(f);
                dynamicReportList.add(e);
            } catch (Exception ex) {
                Logger.getLogger("global").log(Level.SEVERE, null, ex);
            }
        }
    }
