private void btnSaveDesignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveDesignActionPerformed
    int index = lstReport.getSelectedIndex();
    DynamicReport obj = dynamicReportList.get(index);
    if (obj==null || obj.getFile()==null) {
        PanelUtil.showError(jPanel1, "Please select a valid report file");
        return;
    }
    JasperReport report = AbstractReport.compileReport(obj.getFile());
    String name = report.getName();
    if (name==null || name.isEmpty() || name.equals("classic")) {
        PanelUtil.showError(jLabel1, "The report does not have a name.");
        return;
    }
    ReportDesigner.saveDesign(obj.getFile(), name, 1000);
}//GEN-LAST:event_btnSaveDesignActionPerformed

