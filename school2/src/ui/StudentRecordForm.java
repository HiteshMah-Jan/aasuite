    String txt = txtSearchField.getText().toUpperCase();
    List lst = DBClient.getList("SELECT a FROM Student a WHERE a.lastName LIKE '",txt,"%' OR a.firstName LIKE '",txt,"%'");
    if (lst==null || lst.size()==0) {
        PanelUtil.showError(btnSearchNow, "No student found.");
    }
    else {
        setupData(lst);
    }    
