    @Override
    public void saveRecord() {
        pnlStudentInfo.getBean().save();

        pnlFather.getBean().save();
        pnlMother.getBean().save();
        pnlGuardian.getBean().save();
        
        AdmissionRequirements1Ext req = (AdmissionRequirements1Ext) pnlAdmissionInfo1.getBean();
        if (req!=null && !req.isEmptyKey()) {
//            update only
            req.save();
        }

        Enrollment lastEnroll = (Enrollment) pnlStudentStatus.getBean();
        if (lastEnroll!=null && !lastEnroll.isEmptyKey()) {
//            update only
            lastEnroll.save();
        }
    }

    protected void setupData(List lst) {
        stud = (Student) lst.get(0);
        setupData(stud);
    }
    
    private void setupData(Student stud) {
        pnlStudentInfo.setBean(stud);
        pnlBusOperator.setBean(stud);
        pnlHomeDetails.setBean(stud);
        pnlStudentInfoPanel.setBean(stud);
        pnlStudentSchoolAttended.setBean(stud);
        pnlCurriculumSubject.setBean(stud);

        //setup the father info
        pnlFather.setBean(stud.extractOrCreateDependent("FATHER"));
        pnlMother.setBean(stud.extractOrCreateDependent("MOTHER"));
        pnlGuardian.setBean(stud.extractOrCreateDependent("GUARDIAN"));
        
        Admission req = (Admission) AbstractIBean.firstRecord("SELECT a FROM Admission a WHERE a.personId="+stud.personId);
        pnlAdmissionInfo1.setBean(req);
        pnlAdmissionInfo2.setBean(req);

        Enrollment lastEnroll = (Enrollment) stud.selectFirstCache("SELECT a FROM Enrollment a WHERE a.studentId="+stud.personId+" ORDER BY a.seq DESC");
        pnlStudentStatus.setBean(lastEnroll);
        pnlStudentStatusSchoolDetail.setBean(lastEnroll);
        pnlStudentStatusTuitionDetail.setBean(lastEnroll);
        
        pnlFinalGradeInfo.setBean(lastEnroll);
        pnlFinalGradeAverage.setBean(lastEnroll);
    }
