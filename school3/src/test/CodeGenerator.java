private void btnAddAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddAllActionPerformed
    Object[] obj = vecFieldsAvailable.toArray();
    if (obj==null) {
        PanelUtil.showError(btnAdd, "No more fields to add.");
        return;
    }
    for (Object object : obj) {
        vecFieldsDisplay.addElement(object);
    }
    for (Object object : obj) {
        vecFieldsAvailable.removeElement(object);
    }
}//GEN-LAST:event_btnAddAllActionPerformed

