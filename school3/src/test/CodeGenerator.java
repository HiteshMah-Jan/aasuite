private void cboDisplayFieldsValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_cboDisplayFieldsValueChanged
    String field = cboDisplayFields.getSelectedValue().toString();
    if (!mapField.containsKey(field)) {
        mapField.put(field, new DisplayImpl());
    }
    DisplayImpl impl = mapField.get(field);
    impl.label=field;
    BeanUtil.copyBean(impl, datDisplay);
}//GEN-LAST:event_cboDisplayFieldsValueChanged

